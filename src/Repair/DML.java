    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        displayData();
        pnlInput.removeAll();
        pnlInput.revalidate();
        pnlInput.repaint();

        columns.clear();
        textFields.clear();

        String table = cboTable.getSelectedItem().toString();
        if(table.equalsIgnoreCase("Select")){
            JOptionPane.showMessageDialog(this,"Select table first");
            return;
        }

        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3307/phoneDB","root","Root");

            PreparedStatement pst = conn.prepareStatement(
                "SELECT COLUMN_NAME FROM INFORMATION_SCHEMA.COLUMNS " +
                "WHERE TABLE_NAME=? ORDER BY ORDINAL_POSITION");

            pst.setString(1, table);
            ResultSet rs = pst.executeQuery();

            while(rs.next()){
                columns.add(rs.getString("COLUMN_NAME"));
            }

            for(String col : columns){
                JLabel lbl = new JLabel(col.toUpperCase());
                lbl.setFont(new Font("Times New Roman", Font.BOLD, 18));

                JTextField txt = new JTextField(10);
                pnlInput.add(lbl);
                pnlInput.add(txt);
                textFields.add(txt);
            }

            conn.close();

        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
     String table = cboTable.getSelectedItem().toString();
    if(table.equalsIgnoreCase("Select")){
        JOptionPane.showMessageDialog(this,"Select table first");
        return;
    }

    if(textFields.isEmpty()){
        JOptionPane.showMessageDialog(this,"Click Confirm first");
        return;
    }

    for(JTextField tf : textFields){
        if(tf.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"Fill the data to insert");
            return;
        }
    }

    String sql = "INSERT INTO " + table + " (";
    for(int i=0;i<columns.size();i++){
        sql += columns.get(i);
        if(i < columns.size()-1) sql += ",";
    }
    sql += ") VALUES (";
    for(int i=0;i<columns.size();i++){
        sql += "?";
        if(i < columns.size()-1) sql += ",";
    }
    sql += ")";

    try(Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phoneDB","root","Root");
        PreparedStatement pst = con.prepareStatement(sql))
    {

        for(int i=0;i<textFields.size();i++){
            pst.setString(i+1, textFields.get(i).getText());
        }

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this,"Inserted successfully!");
        displayData();

    }catch(Exception ex){
       JOptionPane.showMessageDialog(this, ex);
    }

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         String table = cboTable.getSelectedItem().toString();

       
        if (table.equalsIgnoreCase("Select")) {
            JOptionPane.showMessageDialog(this, "Select table first");
            return;
        }

        if (textFields.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Click Confirm first");
            return;
        }

        int row = tblData.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a row to update");
            return;
        }

        
        String originalPK = tblData.getValueAt(row, 0).toString();
        String enteredPK  = textFields.get(0).getText();

        if (!originalPK.equals(enteredPK)) {
            JOptionPane.showMessageDialog(this,
                    "Primary key cannot be updated!",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE);

           
            textFields.get(0).setText(originalPK);
            return;
        }

       
        for (int i = 1; i < textFields.size(); i++) {
            if (textFields.get(i).getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fields cannot be empty");
                return;
            }
        }

       
        String pkColumn = columns.get(0);

        String sql = "UPDATE " + table + " SET ";
        for (int i = 1; i < columns.size(); i++) {
            sql += columns.get(i) + "=?";
            if (i < columns.size() - 1) sql += ",";
        }
        sql += " WHERE " + pkColumn + "=?";

     
        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3307/phoneDB", "root", "Root");
             PreparedStatement pst = con.prepareStatement(sql)) {

            int index = 1;

            // set non-PK values
            for (int i = 1; i < textFields.size(); i++) {
                pst.setString(index++, textFields.get(i).getText());
            }

          
            pst.setString(index, originalPK);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Updated successfully!");
            displayData();
            tblData.clearSelection();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

