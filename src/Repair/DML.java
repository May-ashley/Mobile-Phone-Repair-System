    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
            String table = cboTable.getSelectedItem().toString();
            if (table.equalsIgnoreCase("Select")) 
            {
                JOptionPane.showMessageDialog(this, "Select table first");
                return;
            }

            int row = tblData.getSelectedRow();

            if (row == -1) 
            {
                JOptionPane.showMessageDialog(this, "Select a row to delete");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(
                this, "Are you sure you want to delete?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);

            if (confirm != JOptionPane.YES_OPTION) return;

            String idColumn = columns.get(0);
            String idValue = tblData.getValueAt(row, 0).toString();

            String sql = "DELETE FROM " + table + " WHERE " + idColumn + "=?";

            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phoneDB", "root", "Root");
                 PreparedStatement pst = con.prepareStatement(sql))
            {

                pst.setString(1, idValue);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Deleted successfully!");
                displayData();

            } 
                    catch (Exception ex) 
            {
                JOptionPane.showMessageDialog(this, ex);
            }
            for (int i = 0; i < textFields.size(); i++) 
            {

                textFields.get(i).setText("");
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

