    private void btnDelColActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelColActionPerformed
        // TODO add your handling code here:
          int i = tblData.getSelectedRow();
    if(i < 0) {
        JOptionPane.showMessageDialog(this, "Please select a column you want to delete");
        return;
    }

    String cName = tblData.getValueAt(i, 0).toString();
    
    // Ask confirmation
    int confirm = JOptionPane.showConfirmDialog(this, 
                    "Are you sure you want to delete column '" + cName + "'?", 
                    "Confirm Delete", 
                    JOptionPane.YES_NO_OPTION);

    if(confirm == JOptionPane.YES_OPTION) {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
            String query = "ALTER TABLE `" + cboTable.getSelectedItem() + "` DROP COLUMN `" + cName + "`";
            PreparedStatement pstmt = con.prepareStatement(query);
            pstmt.execute();
            JOptionPane.showMessageDialog(this, "Column '" + cName + "' is successfully deleted!");
            displayData();
            pstmt.close();
            con.close();
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        }
    }//GEN-LAST:event_btnDelColActionPerformed

    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblData.getSelectedRow();
    if(selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a column to modify!");
        return;
    }

   
    String tableName = cboTable.getSelectedItem().toString();
    String oldColName = tblData.getValueAt(selectedRow, 0).toString();

    
    String newColName = txtName.getText().trim();
    if(newColName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a new column name!");
        return;
    }

    if(cboType.getSelectedItem().equals("Select")) {
        JOptionPane.showMessageDialog(this, "Please select a column type!");
        return;
    }
    String newColType = cboType.getSelectedItem().toString();
    String nullOption = chk1.isSelected() ? "NULL" : "NOT NULL";

    
    String sql = "ALTER TABLE `" + tableName + "` CHANGE `" + oldColName + "` `" + newColName + "` " + newColType + " " + nullOption;

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
        PreparedStatement pstmt = con.prepareStatement(sql);
        pstmt.execute();
        JOptionPane.showMessageDialog(this, "Column '" + oldColName + "' modified successfully to '" + newColName + "'!");
        
        
        displayData();

        pstmt.close();
        con.close();
    } catch(Exception ex) {
        JOptionPane.showMessageDialog(this, ex);
       }
    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnDelTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelTableActionPerformed
        // TODO add your handling code here:
       if(cboTable.getSelectedItem().equals("Select")) {
        JOptionPane.showMessageDialog(this, "Please select a table to delete...");
        return;
    }

    String tName = cboTable.getSelectedItem().toString();

    
    if(tName.equals("customertable") || tName.equals("phonetable") || tName.equals("usertable")) {
        JOptionPane.showMessageDialog(this, "Cannot delete this table!");
        return;
    }


    int confirm = JOptionPane.showConfirmDialog(this, 
                    "Are you sure you want to delete table '" + tName + "'?", 
                    "Confirm Delete", 
                    JOptionPane.YES_NO_OPTION);

    if(confirm == JOptionPane.YES_OPTION) {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
            String sql = "DROP TABLE `" + tName + "`";
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.execute();
            JOptionPane.showMessageDialog(this, "Table '" + tName + "' is deleted.");
            
            cboTable.removeItem(tName);
            cboTable.setSelectedIndex(0);
            DefaultTableModel m = (DefaultTableModel) tblData.getModel();
            m.setRowCount(0);

            pstmt.close();
            con.close();
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    }//GEN-LAST:event_btnDelTableActionPerformed

