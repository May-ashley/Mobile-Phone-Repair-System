    private void displayData(){
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
            //String sql = "select * from " + cboTable.getSelectedItem();
            String sql = "describe "+cboTable.getSelectedItem()+"";
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            tblData.setModel(DbUtils.resultSetToTableModel(rs));
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String dType = cboType.getSelectedItem().toString();
        String st;
        if(chk1.isSelected())
        {
            st = "NULL";
        }
        else
        {
            st = "NOT NULL";

        }
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
            String sql = "alter table "+cboTable.getSelectedItem()+" add "+name+" "+dType+" "+st+"";
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.execute();
            JOptionPane.showMessageDialog(this,"New Column is added...");
            displayData();

        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

