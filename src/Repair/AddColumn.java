    private void addTable()
    {
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
            DatabaseMetaData dbmd = con.getMetaData();
            String[] name = {"TABLE"};
            ResultSet rs = dbmd.getTables("phonedb", null, "%", name);
            while(rs.next())
            {
                cboTable.addItem(rs.getString("TABLE_NAME"));
            }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }

