    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        String selectedOption = cboSelect.getSelectedItem().toString();
        if(selectedOption.equals("Manage Tables (DDL)"))
        {
            Menu_for_DDL md = new Menu_for_DDL();
            this.setVisible(false);
            md.setVisible(true);
            
        }
        else if(selectedOption.equals("Manage Content (DML)"))
        {
            DML d2 = new DML();
            this.setVisible(false);
            d2.setVisible(true);
           
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Please select a vaild option!");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

