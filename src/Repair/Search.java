    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
             String keyword = txtSearch.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter your search data!!!");
            return;
        }

        String selectedTable = cboTable.getSelectedItem().toString();
        if (selectedTable.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select a table first!");
            return;
        }

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phoneDB", "root", "Root");

          
            DatabaseMetaData dbmd = con.getMetaData();
            ResultSet rsCols = dbmd.getColumns(null, null, selectedTable, "%");
            StringBuilder whereClause = new StringBuilder();
            
            while (rsCols.next()) {
                String col = rsCols.getString("COLUMN_NAME");
                if (whereClause.length() > 0) whereClause.append(" OR ");
                whereClause.append(col + " LIKE '%" + keyword + "%'");
            }
            rsCols.close();

  
            String sql = "SELECT * FROM " + selectedTable + " WHERE " + whereClause.toString();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

         
            DefaultTableModel model = new DefaultTableModel();
            tblData.setModel(model);

            ResultSetMetaData rsmd = rs.getMetaData();
            int colCount = rsmd.getColumnCount();
            for (int i = 1; i <= colCount; i++) {
                model.addColumn(rsmd.getColumnName(i));
            }

            int rowCount = 0;
            while (rs.next()) {
                Object[] row = new Object[colCount];
                for (int i = 1; i <= colCount; i++) {
                    row[i - 1] = rs.getObject(i);
                }
                model.addRow(row);
                rowCount++;
            }

            if (rowCount > 0) {
                JOptionPane.showMessageDialog(this, "'" + keyword + "' is Found!");
            } else {
                JOptionPane.showMessageDialog(this, "'" + keyword + "' is Not Found!");
            }

            rs.close();
            stmt.close();
            con.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

    }//GEN-LAST:event_btnSearchActionPerformed

