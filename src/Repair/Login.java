    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(txtUser.getText().equals("")||txtPassword.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Enter username or password...");
        }
        else 
        {
            try
            {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/phonedb","root","Root");
                Statement stmt = con.createStatement();
                String sql = "select * from UserTable where user_ID='"+txtUser.getText()+"'and password ='"+txtPassword.getText()+"'";
                ResultSet rs = stmt.executeQuery(sql);
                int count = 0;
                while(rs.next())
                {
                    count++;
                }
                if(count==1)
                {
                    JOptionPane.showMessageDialog(this,"Admin "+txtUser.getText()+ " Login Success");
                    MainMenu MD = new MainMenu();
                    MD.setVisible(true);
                    this.setVisible(false);
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Invalid Login..");
                    txtUser.setText("");
                    txtPassword.setText("");
                }
                        
                
            }catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this,ex);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

